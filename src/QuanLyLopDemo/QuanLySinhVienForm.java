    private void tblSVMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblSVMouseClicked
        int chonDong = tblSV.getSelectedRow();
        
        if(chonDong>=0){
            String maSinhVien = (String) tblSV.getValueAt(chonDong, 0);
            
            for(SinhVien sv : list){
                if(maSinhVien.equals(sv.getMaSv())){
                    txtMaSV.setText(maSinhVien);
                    txtTenSV.setText(sv.getTenSv());
                    txtSdtSV.setText(""+sv.getSdt());
                    txtSdtGD.setText(String.valueOf(sv.getSdtGd()));
                    cbHanhKiem.setSelectedItem(sv.getHanhKiem());
                    txtDiem.setText(String.valueOf(sv.getDiem()));
                    chkLenLop.setSelected(sv.ísLenLop());
                    break;
                }
            }
        }
    }//GEN-LAST:event_tblSVMouseClicked

