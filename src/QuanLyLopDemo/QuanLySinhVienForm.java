    private void fillTable(){
        tblModel.setRowCount(0);
        
        for(SinhVien sinhVien : list){
            if(sinhVien.getMaLop().equals(maLop)){
                tblModel.addRow(new Object[]{sinhVien.getMaSv(),sinhVien.getTenSv(),sinhVien.getSdt(),
                       sinhVien.getSdtGd(),sinhVien.getDiem(),sinhVien.getHanhKiem(),sinhVien.ísLenLop()});
            }
        }
        
        tblModel.fireTableDataChanged();
    }
