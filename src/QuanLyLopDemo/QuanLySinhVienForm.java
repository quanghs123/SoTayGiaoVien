    private void btnThemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnThemActionPerformed
        StringBuilder sb = new StringBuilder();
        if(txtMaSV.getText().equals("")){
            sb.append("Mã Sinh viên không được để trống \n");
            txtMaSV.setBackground(Color.red);
        }else{
            txtMaSV.setBackground(Color.white);
        }
        if(txtTenSV.getText().equals("")){
            sb.append("Tên Sinh viên không được để trống \n");
            txtTenSV.setBackground(Color.red);
        }else{
            txtTenSV.setBackground(Color.white);
        }
        if(txtSdtSV.getText().equals("")){
            sb.append("Số điện thoại sinh viên không được để trống \n");
            txtSdtSV.setBackground(Color.red);
        }else{
            txtSdtSV.setBackground(Color.white);
        }
        if(txtSdtGD.getText().equals("")){
            sb.append("Số điện thoại gia đình không được để trống \n");
            txtSdtGD.setBackground(Color.red);
        }else{
            txtSdtGD.setBackground(Color.white);
        }
        if(txtDiem.getText().equals("")){
            sb.append("Điểm không được để trống \n");
            txtDiem.setBackground(Color.red);
        }else{
            txtDiem.setBackground(Color.white);
        }
        for(SinhVien sinhVien : list){
            if(sinhVien.getMaLop().equals(maLop)){
                sb.append("Mã Sinh viên đã tồn tại \n");
                txtMaSV.setBackground(Color.red);
            }
        }
        
        if(sb.length()>0){
            JOptionPane.showMessageDialog(this, sb.toString(),"Error",JOptionPane.ERROR_MESSAGE);
            return;
        }
        SinhVien sv = new SinhVien();
        try {
            sv.setMaSv(txtMaSV.getText());
        } catch (Exception e) {
            System.out.println(e.getMessage());
        }
        sv.setTenSv(txtTenSV.getText());
        sv.setSdt(Integer.parseInt(txtSdtSV.getText()));
        sv.setSdtGd(Integer.parseInt(txtSdtGD.getText()));
        sv.setHanhKiem(cbHanhKiem.getSelectedItem().toString());
        sv.setDiem(Float.parseFloat(txtDiem.getText()));
        sv.setMaLop(maLop);
        list.add(sv);
        file.ghi(list, "dbSinhVien.txt");
        fillTable();
    }//GEN-LAST:event_btnThemActionPerformed

    private void btnXoaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnXoaActionPerformed
        StringBuilder sb = new StringBuilder();
        if(txtMaSV.getText().equals("")){
            sb.append("Mã Sinh viên không được để trống \n");
            txtMaSV.setBackground(Color.red);
        }else{
            txtMaSV.setBackground(Color.white);
        }
        if(sb.length()>0){
            JOptionPane.showMessageDialog(this, sb.toString(),"Error",JOptionPane.ERROR_MESSAGE);
            return;
        }
        int luaChon = JOptionPane.showConfirmDialog(this, "Bạn có muốn xóa thông tin không?","Hỏi",JOptionPane.YES_NO_OPTION);
        if(luaChon == JOptionPane.NO_OPTION){
            return;
        }
        for(SinhVien sv : list){
            if(sv.getMaSv().equals(txtMaSV.getText())){
                list.remove(sv);
                break;
            }
        }
        file.ghi(list, "dbSinhVien.txt");
        
        fillTable();
        
        btnNhapMoiActionPerformed(evt);
    }//GEN-LAST:event_btnXoaActionPerformed

