    private void fillTable(){
        tblModel.setRowCount(0);
        
        list = file.doc("dbTbr.txt");
        
        for(TBRutHocPhan tb : list){
            if(tb.getMaGv().equals(maGv)){
                tblModel.addRow(new Object[]{tb.getMaTb(),tb.getMaSv(),tb.getNoiDung(),});
            }
        }
        
        
        tblModel.fireTableDataChanged();
    }
    
    private void initTable(){
        String[] cot = new String[]{"Mã Thông báo","Mã Sinh Viên","Nội dung"};
        
        tblModel.setColumnIdentifiers(cot);
        
        tblTb.setModel(tblModel);
    }
    private int check(String maSv){
        int dem = 0;
        ArrayList<Lop> listDemo = new ArrayList<>();
        for(Lop lop : listLop){
            if(lop.getMaGiaoVien().equals(maGv)){
                listDemo.add(lop);
            }
        }
        for(SinhVien sv : listSv){
            for(Lop lop : listDemo){
                if(sv.getMaSv().equals(txtMaSinhVien.getText())&&sv.getMaLop().equals(lop.getMaLop())){
                    dem++;
                }
            }
        }
        if(dem==0){
            return 0;
        }else return 1;
    }

