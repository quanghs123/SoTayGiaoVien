    private void tblTbMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblTbMouseClicked
        // TODO add your handling code here:
        int chonDong = tblTb.getSelectedRow();
        
        if(chonDong>=0){
            String maTb = (String) tblTb.getValueAt(chonDong, 0);
            
            for(TBRutHocPhan tb : list){
                if(maTb.equals(tb.getMaTb())){
                    txtMa.setText(tb.getMaTb());
                    txtNoiDung.setText(tb.getNoiDung());
                    txtMaSinhVien.setText(tb.getMaSv());
                    break;
                }
            }
        }
    }//GEN-LAST:event_tblTbMouseClicked

    private void txtThemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_txtThemActionPerformed
        // TODO add your handling code here:
        StringBuilder sb = new StringBuilder();
        if(txtMa.getText().equals("")){
            sb.append("Mã thông báo không được để trống \n");
            txtMa.setBackground(Color.red);
        }else{
            txtMa.setBackground(Color.white);
        }
        if(txtMaSinhVien.getText().equals("")){
            sb.append("Mã sinh viên không được để trống \n");
            txtMaSinhVien.setBackground(Color.red);
        }else{
            txtMaSinhVien.setBackground(Color.white);
        }
        if(txtNoiDung.getText().equals("")){
            sb.append("Nội dung thông không được để trống \n");
            txtNoiDung.setBackground(Color.red);
        }else{
            txtNoiDung.setBackground(Color.white);
        }
        for(TBRutHocPhan tb : list){
            if(txtMa.getText().equals(tb.getMaTb())){
                sb.append("Mã thông báo đã tồn tại \n");
                txtMa.setBackground(Color.red);
            }
        }
        if(check(txtMaSinhVien.getText())==0){
            sb.append("Sinh viên không thuộc quản lý của bạn \n");
            txtMa.setBackground(Color.red);
        }
        if(sb.length()>0){
            JOptionPane.showMessageDialog(this, sb.toString(),"Error",JOptionPane.ERROR_MESSAGE);
            return;
        }
        TBRutHocPhan tb = new TBRutHocPhan();
        try {
            tb.setMaTb(txtMa.getText());
        } catch (Exception e) {
            System.out.println(e.getMessage());
        }
        tb.setNoiDung(txtNoiDung.getText());
        tb.setMaSv(txtMaSinhVien.getText());
        tb.setMaGv(maGv);
        
        list.add(tb);
        
        file.ghi(list, "dbTbr.txt");
        
        fillTable();
    }//GEN-LAST:event_txtThemActionPerformed

    private void txtXoaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_txtXoaActionPerformed
        // TODO add your handling code here:
        StringBuilder sb = new StringBuilder();
        if(txtMa.getText().equals("")){
            sb.append("Mã thông báo không được để trống \n");
            txtMa.setBackground(Color.red);
        }else{
            txtMa.setBackground(Color.white);
        }
        if(sb.length()>0){
            JOptionPane.showMessageDialog(this, sb.toString(),"Error",JOptionPane.ERROR_MESSAGE);
            return;
        } 
        int luaChon = JOptionPane.showConfirmDialog(this, "Bạn có muốn xóa thông tin không?","Hỏi",JOptionPane.YES_NO_OPTION);
        if(luaChon == JOptionPane.NO_OPTION){
            return;
        }
        for(TBRutHocPhan tb : list){
            if(tb.getMaTb().equals(txtMa.getText())){
                list.remove(tb);
                break;
            }
        }
        file.ghi(list, "dbTbr.txt");
        
        fillTable();
        
        btnNhapMoiActionPerformed(evt);
    }//GEN-LAST:event_txtXoaActionPerformed

