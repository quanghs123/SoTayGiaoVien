    private void btnThemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnThemActionPerformed
            // TODO add your handling code here:
        StringBuilder sb = new StringBuilder();
        if(txtMa.getText().equals("")){
            sb.append("Mã thông báo không được để trống \n");
            txtMa.setBackground(Color.red);
        }else{
            txtMa.setBackground(Color.white);
        }
        if(txtMaSinhVien.getText().equals("")){
            sb.append("Mã sinh viên không được để trống \n");
            txtMaSinhVien.setBackground(Color.red);
        }else{
            txtMaSinhVien.setBackground(Color.white);
        }
        if(txtNoiDung.getText().equals("")){
            sb.append("Nội dung thông báo không được để trống \n");
            txtNoiDung.setBackground(Color.red);
        }else{
            txtNoiDung.setBackground(Color.white);
        }
        for(BaoThi tb : list){
            if(txtMa.getText().equals(tb.getMaBt())){
                sb.append("Mã thông báo đã tồn tại \n");
                txtMa.setBackground(Color.red);
            }
        }
        if(check(txtMaSinhVien.getText())==0){
            sb.append("Sinh viên không thuộc quản lý của bạn \n");
            txtMa.setBackground(Color.red);
        }
        if(sb.length()>0){
            JOptionPane.showMessageDialog(this, sb.toString(),"Error",JOptionPane.ERROR_MESSAGE);
            return;
        }
        BaoThi tb = new BaoThi();
        try {
            tb.setMaBt(txtMa.getText());
        } catch (Exception e) {
            System.out.println(e.getMessage());
        }
        tb.setNoiDung(txtNoiDung.getText());
        tb.setMaSv(txtMaSinhVien.getText());
        tb.setMaGv(maGv);
        
        list.add(tb);
        
        file.ghi(list, "dbBaoThi.txt");
        
        fillTable();
    }//GEN-LAST:event_btnThemActionPerformed

