    private void btnXoaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnXoaActionPerformed
        // TODO add your handling code here:
        StringBuilder sb = new StringBuilder();
        if(txtMa.getText().equals("")){
            sb.append("Mã thông báo không được để trống \n");
            txtMa.setBackground(Color.red);
        }else{
            txtMa.setBackground(Color.white);
        }
        if(sb.length()>0){
            JOptionPane.showMessageDialog(this, sb.toString(),"Error",JOptionPane.ERROR_MESSAGE);
            return;
        } 
        int luaChon = JOptionPane.showConfirmDialog(this, "Bạn có muốn xóa thông tin không?","Hỏi",JOptionPane.YES_NO_OPTION);
        if(luaChon == JOptionPane.NO_OPTION){
            return;
        }
        for(BaoThi tb : list){
            if(tb.getMaBt().equals(txtMa.getText())){
                list.remove(tb);
                break;
            }
        }
        file.ghi(list, "dbBaoThi.txt");
        
        fillTable();
        
        btnNhapMoiActionPerformed(evt);
    }//GEN-LAST:event_btnXoaActionPerformed

    private void btnCapNhatActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCapNhatActionPerformed
        // TODO add your handling code here:
        StringBuilder sb = new StringBuilder();
        if(txtMa.getText().equals("")){
            sb.append("Mã thông báo không được để trống \n");
            txtMa.setBackground(Color.red);
        }else{
            txtMa.setBackground(Color.white);
        }
        if(txtMaSinhVien.getText().equals("")){
            sb.append("Mã sinh viên không được để trống \n");
            txtMaSinhVien.setBackground(Color.red);
        }else{
            txtMaSinhVien.setBackground(Color.white);
        }
        if(txtNoiDung.getText().equals("")){
            sb.append("Nội dung thông báo không được để trống \n");
            txtNoiDung.setBackground(Color.red);
        }else{
            txtNoiDung.setBackground(Color.white);
        }
        if(check(txtMaSinhVien.getText())==0){
            sb.append("Sinh viên không thuộc quản lý của bạn \n");
            txtMa.setBackground(Color.red);
        }
        if(sb.length()>0){
            JOptionPane.showMessageDialog(this, sb.toString(),"Error",JOptionPane.ERROR_MESSAGE);
            return;
        } 
        int luaChon = JOptionPane.showConfirmDialog(this, "Bạn có muốn cập nhật thông tin không?","Hỏi",JOptionPane.YES_NO_OPTION);
        if(luaChon == JOptionPane.NO_OPTION){
            return;
        }
        for(BaoThi tb : list){
            if(tb.getMaBt().equals(txtMa.getText())){
                tb.setNoiDung(txtNoiDung.getText());
                tb.setMaSv(txtMaSinhVien.getText());
                break;
            }
        }
        file.ghi(list, "dbBaoThi.txt");
        
        fillTable();
        
        btnNhapMoiActionPerformed(evt);
    }//GEN-LAST:event_btnCapNhatActionPerformed

    private void tblBtMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblBtMouseClicked
        // TODO add your handling code here:
        int chonDong = tblBt.getSelectedRow();
        
        if(chonDong>=0){
            String maTb = (String) tblBt.getValueAt(chonDong, 0);
            
            for(BaoThi tb : list){
                if(maTb.equals(tb.getMaBt())){
                    txtMa.setText(tb.getMaBt());
                    txtNoiDung.setText(tb.getNoiDung());
                    txtMaSinhVien.setText(tb.getMaSv());
                    break;
                }
            }
        }
    }//GEN-LAST:event_tblBtMouseClicked

